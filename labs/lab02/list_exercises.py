def reverse_list(integers):
    
    #reverse function reverses the elements of a list

    return integers.reverse()

def minimum(integers):
    
    #min function reads through a list and finds the smallest number
    smallest = min(integers)

    return smallest

    

def sum_list(integers):
    
    #sum function will add all the numbers in the list and return the value of all the elements 
    total = sum(integers)
    
    return total

